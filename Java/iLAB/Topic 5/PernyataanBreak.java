public class PernyataanBreak {
    public static void main(String args[]) {
        int a;
        a = 3;
        switch (a) {
            case 1:
                System.out.println("Anda memilih Java");
                break;
            case 2:
                System.out.println("Anda memilih MySQL");
                break;
            case 3:
                System.out.println("Anda memilih HTML");
                break;
            case 4:
                System.out.println("Anda memilih Python");
                break;
            default:
                System.out.println("Pilihan Kosong");
        }
    }
}
