public class operasiFloatingpoint {
    public static void main(String[] args) {
        double a = 4.25;
        double b = 1.5;

        System.out.println("a * b = " + (a * b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a + b = " + (a + b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
