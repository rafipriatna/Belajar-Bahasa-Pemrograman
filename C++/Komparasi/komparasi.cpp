#include <iostream>

using namespace std;

int main(){
    int a = 2;
    int b = 3;

    // Perbandingan
    // 1 = true
    // 0 = false

    // Sebanding ==
    cout << (a == b) << endl;

    // Tidak sebanding !=
    cout << (a != b) << endl;

    // Kurang dari
    cout << (a < b) << endl;

    // Lebih dari
    cout << (a > b) << endl;

    // Kurang dari sama dengan
    cout << (a <= b) << endl;

    // Lebih dari sama dengan
    cout << (a >= b) << endl;

}