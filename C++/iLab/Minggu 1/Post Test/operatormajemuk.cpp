// Program operasi majemuk
#include <iostream>
using namespace std;

int main(){
    // Deklarasi variabel
    int x = 2; // Mula-mula, x bernilai 2
    
    // Output
    cout << "x = " << x << endl;
    
    x += 2;
    cout << "Setelah x += 2, x = " << x << endl;
    
    x *= 2;
    cout << "Setelah x *= 2, x = " << x << endl;
    
    return 0;
}